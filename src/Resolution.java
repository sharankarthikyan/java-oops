public class Resolution {
    // Attributes of Resolution
    private int width;
    private int height;

    // Initialization
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    // Getters
    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
